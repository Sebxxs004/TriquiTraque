    private void btn01ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn01ActionPerformed
        if(turnoP1){
            txtTurno.setForeground(Nav.game.player2.getColor());
            turnoP1 = false;
        } else {
            txtTurno.setForeground(Nav.game.player1.getColor());
            turnoP1 = true;
        }
        movimientos++;
        txtMov.setText(String.valueOf(movimientos));
        btn01.setText(txtTurno.getText());
        btn01.setEnabled(false);
        Nav.board.colocarFicha();
        if("X".equals(txtTurno.getText())){
            tab5x5[0][0] = 1;
        } else {
            tab5x5[0][0] = 2;
        }
        ganador = Nav.game.hayGanador5x5(tab5x5);
        Nav.game.elegirGanador(ganador, movimientos);
    }//GEN-LAST:event_btn01ActionPerformed

